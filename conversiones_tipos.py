# Conversión de decimal a entero
decimal = 3.14
entero = int(decimal)
print("Decimal a entero:", entero)

# Conversión de entero a cadena
numero = 42
cadena = str(numero)
print("Entero a cadena:", cadena)
print("Tipo de 'cadena':", type(cadena))

# Conversión de cadena a flotante
cadena_flotante = "3.14"
flotante = float(cadena_flotante)
print("Cadena a flotante:", flotante)

# Conversión de cadena a entero
cadena_entero = "123"
entero_desde_cadena = int(cadena_entero)
print("Cadena a entero:", entero_desde_cadena)

# Conversión de entero a flotante
entero_a_convertir = 42
flotante_desde_entero = float(entero_a_convertir)
print("Entero a flotante:", flotante_desde_entero)

# Conversión de flotante a cadena
flotante_a_convertir = 2.718
cadena_desde_flotante = str(flotante_a_convertir)
print("Flotante a cadena:", cadena_desde_flotante)
print("Tipo de 'cadena_desde_flotante':", type(cadena_desde_flotante))

# Conversión de booleano a entero
booleano = True
entero_desde_booleano = int(booleano)
print("Booleano a entero:", entero_desde_booleano)

# Conversión de entero a booleano
entero_a_booleano = 0
booleano_desde_entero = bool(entero_a_booleano)
print("Entero a booleano:", booleano_desde_entero)

# Conversión de cadena a booleano
cadena_a_booleano = "False"
booleano_desde_cadena = bool(cadena_a_booleano)
print("Cadena a booleano:", booleano_desde_cadena)

# Conversión de booleano a cadena
booleano_a_cadena = False
cadena_desde_booleano = str(booleano_a_cadena)
